package com.wolken.market.map;

public class Encapsulated {

	private int id;
	private String name;
	private String location;
	private int no_of_airlines;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public int getNo_of_airlines() {
		return no_of_airlines;
	}
	public void setNo_of_airlines(int no_of_airlines) {
		this.no_of_airlines = no_of_airlines;
	}
	
	
	
	
}
