class Scientist {
	private String name;
	private byte yearOfExp;
	private String role;
	private String type;
	private String address;
	
	void setName(String name){
		this.name=name;
	}
	void setYearOfExp(byte yearOfExp){
		this.yearOfExp=yearOfExp;
	}
	void setRole(String role){
		this.role=role;
	}
	void setType(String type){
		this.type=type;
	}
	void setAddress(String address){
		this.address=address;
	}
	
	String getName(){
		return name;
	}
	byte getYearOfExp(){
		return yearOfExp;
	}
	String getRole(){
	    return role;
		}
	String getType(){
	    return type;
		}
	String getAddress(){
	    return address;
		}
		
	}
		
		